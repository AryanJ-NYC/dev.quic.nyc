              <p>Governor Andrew M. Cuomoo today announced the first round of NY CUNY 2020 awards| providing $55 million to fund
              eight innovative projects involving 20 participating CUNY campuses designed to connect academic excellence with
              entrepreneurship and local economic development. Projects were selected through a competitive process utilizing staff
              from CUNY and Empire State Development and will support the creation of more than 3|800 jobs over six years.</p>
              <p>&quot;The students at New York City’s public colleges and universities have provided the world with
              game-changing ideas in fields ranging from healthcare to manufacturing. Through the CUNY2020 program we are
              maximizing those ideas to create economic opportunity for New Yorkers,” Governor Cuomo said. “The first round of
              CUNY 2020 projects will help link some of our best and brightest students with positions in high-tech sectors, while
              leveraging their academic excellence to support economic development and create new opportunities throughout the New
              York City area. By funding these eight projects we are making an investment in New York’s future, and I am proud to
              present these awards to the first round of CUNY 2020 recipients.”</p>
              <p>CUNY Chancellor James B. Milliken said, “We commend Governor Cuomo and the State of New York for investing in
              The City University of New York with CUNY 2020. CUNY has developed and implemented many public/private partnerships
              and this vitally important initiative will continue to grow the economy, create employment opportunities, and expand
              our research and development in all five boroughs. This is a great day for the University and all New Yorkers.”</p>
              <p>The NY CUNY 2020 program links the knowledge and innovation of higher education to regional economic
              revitalization, and the Governor appropriated $55 million as part of the 2013-14 State Budget for NY CUNY 2020. The
              program offers grants for 2- and 4-year colleges within the CUNY system for projects that connect economic
              development and academic excellence. Projects are selected in a competitive manner based on economic impact,
              advancement of academic goals, innovation, and collaboration.</p>
              <p>Advanced Manufacturing for Economic Development (awarded $1.5 million): Queensborough Community College; Queens
              College</p>
              <p>This project will develop an Advanced Manufacturing Center focused on 3-D printing, to be renovated and equipped
              within 6 months with 3-D printing equipment. Students will gain the skills to design and produce sophisticated
              components through collaboration with the Queens College Computer Science Department and local high schools, which
              will create new courses for students to learn sophisticated program and parts processes.</p>
              <p>3-D printing is a technological process that can considerably lower manufacturing costs and return companies to
              the domestic market. Manufacturers need workers, as several hundred graduates over a 5 year period will be produced
              in this emerging technology, manufacturers will have increased capability to produce parts domestically that are
              currently developed overseas.</p>
